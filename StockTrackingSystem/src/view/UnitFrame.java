    public void initTable(){
        UnitService service = new UnitService();
        List<Unit> unitList = new ArrayList();
        unitList = service.getAllUnit();
        for(int i=0;i<unitList.size();i++){
            String idValue = String.valueOf(unitList.get(i).getId());
            String unitValue = unitList.get(i).getUnit();
            System.out.println(idValue);            
            System.out.println(unitValue);

            String tbData[] = {idValue,unitValue};
            DefaultTableModel tablemodel = (DefaultTableModel) this.jTable1.getModel();
            tablemodel.addRow(tbData);
        }
    }
    
    public void updateTable(){
        clearTable();
        initTable();
    }
