    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        UnitService service = new UnitService();
        Unit unit = new Unit();
        Integer idValue = null;
        try{
            idValue = Integer.parseInt(jTextField2.getText());
        }catch(Exception err){
            System.out.println(err);
        }
        if(idValue!=null){
            unit.setId(idValue);
            service.deleteUnit(unit);
            clearOtherTextField();
            updateTable();
            JOptionPane.showMessageDialog(null, "Seçmiş olduğunuz birim silinmiştir.");
        }else{
            JOptionPane.showMessageDialog(null, "Id değeri tam sayı olmalıdır.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        UnitService service = new UnitService();
        Unit unit = new Unit();
        Integer idValue = null;
        String unitValue = null;
        try{
            idValue = Integer.parseInt(jTextField2.getText());
        }catch(Exception err){
            System.out.println(err);
        }
        unitValue = jTextField3.getText();
        System.out.println(idValue + " =>  " + unitValue);
        if(idValue!=null && unitValue!= null){
            unit.setId(idValue);
            unit.setUnit(unitValue);
            service.updateUnit(unit,key);
            clearOtherTextField();
            updateTable();
            JOptionPane.showMessageDialog(null, "Seçmiş olduğunuz birim güncellenmiştir.");
        }else{
            JOptionPane.showMessageDialog(null, "Id değeri tam sayı olmalıdır.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

